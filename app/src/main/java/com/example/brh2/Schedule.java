    public int getDate(String s) {
        String m = s.substring(17,18);
        if (m.equals("P")) { m = "1";}
        else {m = "0";}
        return Integer.parseInt(s.substring(3,5) + m + s.substring(11,13));
    }

    public void fixName(Exam e)
    {
        String info = e.name.toUpperCase();
        if (info.length() < 11) {
            if ((!(data.get(info + " 001") == (null))))
                info = info + " 001";
            else if (!(data.get(info + " 101") == (null)))
                info = info + " 101";
            else if (!(data.get(info + " 201") == (null)))
                info = info + " 201";
            else if (!(data.get(info + " 301") ==(null)))
                info = info + " 301";
            else if (!(data.get(info + " 401") ==(null)))
                info = info + " 401";
            else if (!(data.get(info + " 002") ==(null)))
                info = info + " 002";
            else if (!(data.get(info + " 003") ==(null)))
                info = info + " 003";
            else if (!(data.get(info + " 004") ==(null)))
                info = info + " 004";
            else if (!(data.get(info + " 005") ==(null)))
                info = info + " 005";
            else
                info = info + " 501";
        }

        e.setName(info);
    }

